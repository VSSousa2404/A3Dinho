    public void carregarTabelaDoadores(List<Doador> doadores) {
        
        DefaultTableModel modelo = (DefaultTableModel) tabelaDoadores.getModel();

        modelo.setRowCount(0);

        for (Doador d : doadores) {
            modelo.addRow(new Object[]{
                d.getNome(),
                d.getCpf(),
                d.getTipoSanguineo(),
                d.getDataDoacao()
            });
        }
}
