    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        String filtroCpf = txtCpf.getText();
        String filtroNome = txtNome.getText();
        String filtroTipo = txtTipo.getText();
        
        List<Doador> listaDoadores = new DoadorDao().listaDoadores(filtroNome, filtroCpf, filtroTipo);
        
        carregarTabelaDoadores(listaDoadores);
    }//GEN-LAST:event_btnBuscarActionPerformed

